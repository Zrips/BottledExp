package me.Zrips.bottledexp;

public class EBlock {

    String World;
    int x;
    int y;
    int z;

    public EBlock() {
    }

    public void setWorld(String World) {
	this.World = World;
    }

    public String getWorld() {
	return this.World;
    }

    public void setX(int x) {
	this.x = x;
    }

    public int getX() {
	return this.x;
    }

    public void setY(int y) {
	this.y = y;
    }

    public int getY() {
	return this.y;
    }

    public void setZ(int z) {
	this.z = z;
    }

    public int getZ() {
	return this.z;
    }
}
